string|'"""Save current exception, run some code and then re-raise.\n\n    In some cases the exception context can be cleared, resulting in None\n    being attempted to be re-raised after an exception handler is run. This\n    can happen when eventlet switches greenthreads or when running an\n    exception handler, code raises and catches an exception. In both\n    cases the exception context will be cleared.\n\n    To work around this, we save the exception state, run handler code, and\n    then re-raise the original exception. If another exception occurs, the\n    saved exception is logged and the new exception is re-raised.\n\n    In some cases the caller may not want to re-raise the exception, and\n    for those circumstances this context provides a reraise flag that\n    can be used to suppress the exception.  For example:\n\n    except Exception:\n        with save_and_reraise_exception() as ctxt:\n            decide_if_need_reraise()\n            if not should_be_reraised:\n                ctxt.reraise = False\n    """'
